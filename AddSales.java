    private void SaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SaveActionPerformed
        // TODO add your handling code here:
        date = DateA.getText().trim();
        company = Cname.getText().trim();
        try {
            ToTal = Integer.parseInt(TotalA.getText().trim());
        } catch (Exception e) {
        }
        guide = Gname.getText().trim();
        companyCom = Ccom.getText().trim();
        notes = Note.getText().trim();
        if (ToTal == 0) {
            JOptionPane.showMessageDialog(new AddSales(), "false Total Sales !");
            Load.setText("failed");
        } else {
            if (!date.equals("") && !company.equals("") && !TotalA.getText().trim().equals("")) {
                InsertData(date, guide, company, ToTal, companyCom, notes);
            } else {
                JOptionPane.showMessageDialog(new AddSales(), "data is empty !");
            }
        }
        if (done) {
            Load.setText("Successfully Saved");
            ToTal = 0;
            DateA.setText("");
            Cname.setText("");
            Gname.setText("");
            TotalA.setText("");
            Ccom.setText("");
            Note.setText("");
        } else {
            Load.setText("failed");
        }


    }//GEN-LAST:event_SaveActionPerformed


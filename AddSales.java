    public void InsertData(String Date, String Guide, String Company, int Total, String CompanyCommision, String Notes) {
        c++;
        done = false;
        try {
            String sql = "INSERT INTO sales (Date,GuideName,CompanyName,TotalSales,CompanyCommission,Notes) VALUES ('" + Date + "','" + Guide + "','" + Company + "'," + Total + ",'" + CompanyCommision + "','" + Notes + "')";
            st.executeUpdate(sql);
            done = true;
        } catch (Exception e) {
            System.out.println(e);
            done = false;
        }
    }

